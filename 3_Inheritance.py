# Inheritance : Another key concept in Object Oriented Programming

# It provides a way to create a new class frrom an existing class.
# The new class is specialized version of existing class such that it inherits all non-private properties and methods from existing class ( like having a relation)

# Examples of relation:
# Rectange IS A shape
# Python IS A programming language

# With above shown relations, we can conclude that we can build new classes by extending existing classes.

# Existing class:           # Derived Class
# Shape                     # Rectangle
# Programming Language      # Python

